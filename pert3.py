# kita akan belajar tipe data (jenis2 data)

#bilangan bulat (integer "int")
x = 10
print("nilai dari x :" , x)
print(type(x))

#bilangan pecahan (fload "fload")
y = 3.5
print("nilai dari y :", y)
print(type(y))

#tipe data string(tulisan "str")
nama = "neza"
print("nilai dari nama :" , nama)
print(type(nama))

#tipe data benar salah (true / false "bool")
kebenaran = False
print("nilai dari kebenaran :" , kebenaran)
print(type(kebenaran))